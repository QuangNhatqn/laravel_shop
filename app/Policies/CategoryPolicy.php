<?php

namespace App\Policies;

use App\Models\Category;
use App\Models\User;
use Illuminate\Auth\Access\HandlesAuthorization;

class CategoryPolicy
{
    use HandlesAuthorization;

    /**
     * Determine whether the user can view any models.
     *
     * @param  \App\Models\User  $user
     * @return mixed
     */
    public function viewAny(User $user)
    {
        //
    }

    /**
     * Determine whether the user can view the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Category  $category
     * @return mixed
     */
    public function view(User $user, $type)
    {
        return $user->checkPermissionAccess(config('permissions.access.' . $type . '-category-list'));
    }

    /**
     * Determine whether the user can create models.
     *
     * @param  \App\Models\User  $user
     * @return mixed
     */
    public function create(User $user, $type)
    {
        return $user->checkPermissionAccess(config('permissions.access.' . $type . '-category-add'));
    }

    /**
     * Determine whether the user can update the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Category  $category
     * @return mixed
     */
    public function update(User $user, $id)
    {
        $category = Category::find($id);
        return $user->checkPermissionAccess(config('permissions.access.' . $category->type . '-category-update'));
    }

    /**
     * Determine whether the user can delete the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Category  $category
     * @return mixed
     */
    public function delete(User $user, $id)
    {
        $category = Category::withTrashed()->find($id);
        return $user->checkPermissionAccess(config('permissions.access.' . $category->type . '-category-delete'));
    }

    /**
     * Determine whether the user can restore the model.
     *
